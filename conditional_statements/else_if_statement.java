package conditional_statements;

public class else_if_statement {

	public static void main(String[] args) {
		int n=0;
		if (n%2==0)
			System.out.println("n is even");
		else if(n%2!=0)
		
			System.out.println("n is odd");
		else
			System.out.println("it is neither odd nor even");

	}

}
